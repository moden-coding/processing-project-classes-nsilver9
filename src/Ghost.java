public class Ghost {
    private float x;
    private float y;
    private float size;

    public Ghost(float x, float y, float size) {
        this.x = x;
        this.y = y;
        this.size = size;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public float getSize() {
        return size;
    }
}
